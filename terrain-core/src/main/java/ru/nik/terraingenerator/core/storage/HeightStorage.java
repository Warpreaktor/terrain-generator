package ru.nik.terraingenerator.core.storage;

/**
 * Абстракция хранения значений высоты.
 *
 * Это позволяет позже заменить сплошной массив на чанковое хранение,
 * не меняя внешнее API сетки.
 */
public interface HeightStorage {

    /**
     * Возвращает значение высоты по внутреннему линейному индексу.
     *
     * @param index линейный индекс
     * @return значение высоты
     */
    float get(int index);

    /**
     * Устанавливает значение высоты по внутреннему линейному индексу.
     *
     * @param index  линейный индекс
     * @param value  новое значение высоты
     */
    void set(int index, float value);

    /**
     * Добавляет смещение к текущему значению высоты.
     *
     * @param index линейный индекс
     * @param delta добавляемое смещение
     */
    void add(int index, float delta);

    /**
     * Возвращает количество элементов в хранилище.
     *
     * @return размер хранилища
     */
    int size();

    /**
     * Создаёт полную копию хранилища.
     *
     * @return независимая копия
     */
    HeightStorage copy();
}
