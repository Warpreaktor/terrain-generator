package ru.nik.terraingenerator.core.operation;

/**
 * Режим преобразования исходного значения шума.
 *
 * Один и тот же генератор шума может давать разный характер рельефа,
 * если по-разному интерпретировать sampled значение.
 */
public enum NoiseTransformMode {
    /**
     * Обычный шум с равномерным уходом в положительные и отрицательные значения.
     */
    STANDARD,

    /**
     * Шум с прижатыми к нулю краями и выраженными грядами.
     */
    RIDGED,

    /**
     * Шум с мягкими буграми и поднятыми положительными зонами.
     */
    BILLOW
}
